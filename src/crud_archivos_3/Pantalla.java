    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre;
        String genero;
        String director;
        int año;
        
        try{
            nombre = jTextField1.getText();
            genero = jTextField2.getText();
            director = jTextField3.getText();
            año = Integer.parseInt(jTextField4.getText());
            pelis.add(new Peliculas(nombre,genero,director,año));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al introducir datos");
        }
        verDatos();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila;
        String nombre;
        String genero;
        String director;
        int año;
        
        Peliculas peliculas;
        
        try{
            fila = jTable1.getSelectedRow();
            peliculas = pelis.get(fila);
            nombre = JOptionPane.showInputDialog("Modifique el nombre");
            genero = JOptionPane.showInputDialog("Modifique el género");
            director = JOptionPane.showInputDialog("Modifique el director");
            año = Integer.parseInt(JOptionPane.showInputDialog("Modifique el año de estreno"));
            peliculas.setNombre(nombre);
            peliculas.setGenero(genero);
            peliculas.setDirector(director);
            peliculas.setAño(año); 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Escoga una fila");
        }
        verDatos();
    }//GEN-LAST:event_jButton2ActionPerformed

