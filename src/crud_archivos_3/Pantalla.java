    public void guardar(){
        File file = new File("Peliculas.txt");
        PrintWriter escribir;
        if(!file.exists()){
            try{
                file.createNewFile();
            }catch(Exception e){
                
            }
        }
        try{
            Peliculas peliculas;
            escribir = new PrintWriter(file,"utf-8");
            for(int i=0;i<pelis.size();i++){
                peliculas = pelis.get(i);
                peliculas.guardar(escribir);
            }
            escribir.close();
        }catch(Exception e){
            
        }
    }
    
    public void verDatos(){
        //vaciar tabla
        DefaultTableModel vaciar_tabla = (DefaultTableModel) jTable1.getModel();
        vaciar_tabla.setNumRows(0);
        jTable1.setModel(vaciar_tabla);
        
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        for (int i=0; i< pelis.size();i++){
            
            Object[] peli = new Object[4];
            peli[0] = pelis.get(i).getNombre();
            peli[1] = pelis.get(i).getGenero();
            peli[2] = pelis.get(i).getDirector();
            peli[3] = pelis.get(i).getAño();
            
            
            modelo.addRow(peli);
            jTable1.setModel(modelo);
        }
    }
    
